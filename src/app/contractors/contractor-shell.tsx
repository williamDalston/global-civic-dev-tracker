'use client';

import Link from 'next/link';
import { usePathname, useRouter } from 'next/navigation';
import { cn } from '@/lib/utils/cn';

const publicPaths = ['/contractors/login', '/contractors/signup', '/contractors/forgot-password'];

const navItems = [
  { href: '/contractors/dashboard', label: 'Dashboard', icon: LayoutDashboardIcon },
  { href: '/contractors/dashboard/leads', label: 'My Leads', icon: UsersIcon },
  { href: '/contractors/dashboard/billing', label: 'Billing', icon: CreditCardIcon },
  { href: '/contractors/dashboard/profile', label: 'Profile', icon: SettingsIcon },
];

export function ContractorShell({ children }: { children: React.ReactNode }) {
  const pathname = usePathname();
  const router = useRouter();

  const isPublicPath = publicPaths.some((p) => pathname.startsWith(p));

  if (isPublicPath) {
    return <>{children}</>;
  }

  async function handleLogout() {
    await fetch('/api/contractors/auth/login', { method: 'DELETE' });
    router.push('/contractors/login');
  }

  return (
    <div className="fixed inset-0 z-50 flex bg-background">
      <aside className="flex w-64 flex-col border-r border-border bg-card">
        <div className="flex h-16 items-center border-b border-border px-6">
          <Link href="/contractors/dashboard" className="flex items-center gap-2">
            <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-primary">
              <HardHatIcon className="h-5 w-5 text-primary-foreground" />
            </div>
            <span className="text-lg font-bold text-foreground">Contractor Portal</span>
          </Link>
        </div>

        <nav className="flex-1 space-y-1 px-3 py-4">
          {navItems.map((item) => {
            const isActive =
              item.href === '/contractors/dashboard'
                ? pathname === '/contractors/dashboard'
                : pathname.startsWith(item.href);

            return (
              <Link
                key={item.href}
                href={item.href}
                className={cn(
                  'flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors',
                  isActive
                    ? 'bg-primary/10 text-primary'
                    : 'text-muted-foreground hover:bg-accent hover:text-foreground'
                )}
              >
                <item.icon className="h-5 w-5" />
                {item.label}
              </Link>
            );
          })}
        </nav>

        <div className="border-t border-border p-3">
          <Link
            href="/"
            className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium text-muted-foreground transition-colors hover:bg-accent hover:text-foreground"
          >
            <HomeIcon className="h-5 w-5" />
            Back to Site
          </Link>
          <button
            onClick={handleLogout}
            className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium text-muted-foreground transition-colors hover:bg-accent hover:text-foreground"
          >
            <LogOutIcon className="h-5 w-5" />
            Log out
          </button>
        </div>
      </aside>

      <div className="flex flex-1 flex-col overflow-hidden">
        <header className="flex h-16 items-center border-b border-border px-8">
          <h2 className="text-sm font-medium text-muted-foreground">
            Global Civic Development Tracker — Contractor Portal
          </h2>
        </header>
        <main className="flex-1 overflow-y-auto p-8">{children}</main>
      </div>
    </div>
  );
}

